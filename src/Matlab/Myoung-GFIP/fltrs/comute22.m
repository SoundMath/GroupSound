clear;
n=32;
n2=n*2;
orgn=1;
%
pp=1;
gg1=bkgd(2,8,n);
gg2=bkgd(4,8,n);
gg=gg1+gg2;
mm=max(max(gg));
sc=128/mm;
lvl=input('input noise level as a multiple of max intensity    ')
sdv=lvl*mm;
mn=0;
ns=noise(n2,n2,mn,sdv);
ngg=gg+ns;
%
gp1=11;
gp2=104;
%
figure(1)
[x1,x21,x22,x23,x4]=nn2_22(ngg,n,gp1,orgn);
ff1=real(inn2_22(x1,n,gp1,orgn));
