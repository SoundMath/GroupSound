xx23=real(inn2_22(x23,n,gp1,orgn));
[x23y1,tem21,x23y22,tem23,tem4]=nn2_22(xx23,n,gp2,orgn);
ff5=real(inn2_22(x23y1,n,gp2,orgn));
ff6=real(inn2_22(x23y22,n,gp2,orgn));
xx4=real(inn2_22(x4,n,gp1,orgn));
[tem1,x4y21,tem22,x4y23,x4y4]=nn2_22(xx4,n,gp2,orgn);
ff7=real(inn2_22(x4y21,n,gp2,orgn));
